import "./index.css"

const MsgBox = (props) => {
    const { message, isSender, onDeleteMsg } = props
    const { msgText, name, color, time, id, msgID } = message

    const itemClass = isSender ? "sent" : "received"

    let d = new Date(time)
    let timeText = d.toLocaleTimeString(navigator.language, {
        hour: '2-digit',
        minute: '2-digit'
    });

    const deleteMsg = () => {
        onDeleteMsg(msgID)
    }

    return (

        <div className={`message ${itemClass}`}>
            {
                itemClass === "received" ?
                    <div className="username" style={{
                        color: color,
                        maxWidth: `${msgText.length}ch`
                    }}>
                        <span title={name} >
                            {name}
                        </span>
                    </div> :
                    <><button className="delete" onClick={deleteMsg}>
                        <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor" className="bi bi-trash3-fill" viewBox="0 0 16 16">
                            <path d="M11 1.5v1h3.5a.5.5 0 0 1 0 1h-.538l-.853 10.66A2 2 0 0 1 11.115 16h-6.23a2 2 0 0 1-1.994-1.84L2.038 3.5H1.5a.5.5 0 0 1 0-1H5v-1A1.5 1.5 0 0 1 6.5 0h3A1.5 1.5 0 0 1 11 1.5Zm-5 0v1h4v-1a.5.5 0 0 0-.5-.5h-3a.5.5 0 0 0-.5.5ZM4.5 5.029l.5 8.5a.5.5 0 1 0 .998-.06l-.5-8.5a.5.5 0 1 0-.998.06Zm6.53-.528a.5.5 0 0 0-.528.47l-.5 8.5a.5.5 0 0 0 .998.058l.5-8.5a.5.5 0 0 0-.47-.528ZM8 4.5a.5.5 0 0 0-.5.5v8.5a.5.5 0 0 0 1 0V5a.5.5 0 0 0-.5-.5Z" />
                        </svg>
                    </button>
                        <button className="threedots">
                            <svg viewBox="0 0 18 18" width="18" height="18" ><path fill="currentColor" d="M3.3 4.6 9 10.3l5.7-5.7 1.6 1.6L9 13.4 1.7 6.2l1.6-1.6z"></path></svg>

                            <div className="delete-small" onClick={deleteMsg}>
                                <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor" className="bi bi-trash3-fill" viewBox="0 0 16 16">
                                    <path d="M11 1.5v1h3.5a.5.5 0 0 1 0 1h-.538l-.853 10.66A2 2 0 0 1 11.115 16h-6.23a2 2 0 0 1-1.994-1.84L2.038 3.5H1.5a.5.5 0 0 1 0-1H5v-1A1.5 1.5 0 0 1 6.5 0h3A1.5 1.5 0 0 1 11 1.5Zm-5 0v1h4v-1a.5.5 0 0 0-.5-.5h-3a.5.5 0 0 0-.5.5ZM4.5 5.029l.5 8.5a.5.5 0 1 0 .998-.06l-.5-8.5a.5.5 0 1 0-.998.06Zm6.53-.528a.5.5 0 0 0-.528.47l-.5 8.5a.5.5 0 0 0 .998.058l.5-8.5a.5.5 0 0 0-.47-.528ZM8 4.5a.5.5 0 0 0-.5.5v8.5a.5.5 0 0 0 1 0V5a.5.5 0 0 0-.5-.5Z" />
                                </svg>
                            </div>


                        </button>

                    </>


            }

            <span className="msgText" >
                {msgText}
            </span>
            <div className="metadata">
                <span className="time">{timeText}</span>
            </div>

        </div>
    )
}

export default MsgBox